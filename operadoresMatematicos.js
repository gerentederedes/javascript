const multiplicacao = numeroUm * numeroDois
console.log('Resultado da multiplicação: ' + multiplicacao) 

const divisao = numeroDois / numeroUm
console.log('Resultado da divisão: ' + divisao) 

const soma = numeroUm + numeroDois
console.log('Resultado da soma: ' + soma)   

const restoDaDivisao = numeroDois % numeroUm
console.log('Resto da divisão: ' + restoDaDivisao)  

let incremento = numeroUm
incremento++
console.log('Valor após incremento: ' + incremento) 

let decremento = numeroDois
decremento--
